// Constants
fn main() {
    const N: u16 = 10;
    println!("The value of {}", N);
    println!("The value of PI {}", PI);
    println!("The value of Three hours in seconds {}", THREE_HOURS_IN_SECONDS);
}


// You can declare a constant with a type annotation
const PI: f64 = 3.141592653;
const THREE_HOURS_IN_SECONDS: u32 = 60 * 60 * 3;